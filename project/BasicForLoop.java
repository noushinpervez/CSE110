package project;

public class BasicForLoop {

	public static void main(String[] args) {
		System.out.print("\n\n\t\tStudytonight - Best place to learn\n\n\n");

	    int i = 0;

	    for(i = 0; i < 10; i++)
	    	System.out.print("i = " + i + "\n");

	    System.out.print("\nThe value of i after exiting the loop is " + i + "\n\n");
		    
	    System.out.print("\nRemember that the loop condition checks the conditional statement before it loops again.\n\n");
		    
	    System.out.print("Consequently, when i equals 10, the loop breaks.\n\n");
		    
	    System.out.print("i is updated before the condition is checked- hence the value of i after exiting the loop is 10 .\n\n");
		    
	    System.out.print("\n\n\t\t\tCoding is Fun !\n\n\n");
	}
}